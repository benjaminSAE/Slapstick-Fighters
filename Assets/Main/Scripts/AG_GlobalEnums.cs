
/// <summary>
/// This enum allows you to access the three character types from any script in your project.
/// </summary>
public enum CharacterType {
    /// <summary>
    /// We haven't set our character type yet.
    /// </summary>
    Undefined = 0,
    /// <summary>
    /// The archer.
    /// </summary>
    Archer = 1,
    /// <summary>
    ///  The knight.
    /// </summary>
    Knight = 2,
    /// <summary>
    /// The tank.
    /// </summary>
    Tank = 3
};
